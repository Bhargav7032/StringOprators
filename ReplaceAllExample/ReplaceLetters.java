package com.ReplaceAllExample;

public class ReplaceLetters {
	public static void main(String[]args) {
		String s1="welcom";
		String s2="hello";
		System.out.println(s1.replace('w','v'));
		System.out.println(s1.replace('c','k'));
		
		System.out.println(s2.replace('e','a'));
		System.out.println(s2.replace('o', 'u'));
	}

}

package com.ReplaceAllExample;

public class ReplaceAll3 {
public static void main(String[] args) {
	String s1="Bhargav";
	String s2="Hari";
	String s3="sai";
	System.out.println(s1.replaceAll("Bhargav", "Hari"));
	System.out.println(s2.replaceAll("Hari","mohanbabu"));
	System.out.println(s3.replaceAll("sai", "chiru"));
}
}
